# 8. 대체용(Fallback) 키워드 기반 카테고리 추출
# 원천 카테고리가 불명확하거나 매핑되지 않을 경우, 제목과 제공 내용을 기반으로 추론합니다.
# ==========================================
def extract_category_by_keyword(title: str, reward_text: str) -> str:
    """제목과 제공 내역 내용을 기반으로 카테고리를 추론하는 함수 (14가지 분류)"""
    combined_text = (title + " " + reward_text).lower()
    
    # 1. 뷰티
    if any(k in combined_text for k in ['에스테틱', '미용실', '헤어', '염색', '펌', '네일', '속눈썹', '왁싱', '피부관리', '피부', '마사지', '테라피', '스파']):
        return "뷰티"
    # 2. 숙박
    if any(k in combined_text for k in ['모텔', '호텔', '펜션', '풀빌라', '글램핑', '캠핑', '게스트하우스']):
        return "숙박"
    # 3. 여행
    if any(k in combined_text for k in ['여행', '투어', '관광', '티켓', '항공', '렌트카']):
         return "여행"
    # 4. 반려동물
    if any(k in combined_text for k in ['강아지', '고양이', '반려동물', '애견', '펫']):
        return "반려동물"
    # 5. 유아동
    if any(k in combined_text for k in ['유아', '아동', '키즈', '장난감', '어린이', '육아', '베이비']):
        return "유아동"
    # 6. 운동
    if any(k in combined_text for k in ['헬스', 'pt', '피티', '필라테스', '요가', '크로스핏', '수영', '운동', '다이어트', '피트니스']):
        return "운동"
    # 7. 레저
    if any(k in combined_text for k in ['레저', '수상', '스키', '보드', '서핑', '클라이밍', '스크린골프', '골프', '볼링', '방탈출', '테마파크', '워터파크']):
        return "레저"
    # 8. 문화
    if any(k in combined_text for k in ['전시', '공연', '연극', '뮤지컬', '영화', '콘서트', '원데이클래스', '공방', '클래스', '사진관', '스튜디오']):
        return "문화"
    # 9. 배달
    if '배달' in combined_text:
        return "배달"
    # 10. 포장
    if any(k in combined_text for k in ['포장', '테이크아웃', '픽업']):
        return "포장"
    # 11. 음식점
    if any(k in combined_text for k in ['맛집', '카페', '고기', '식당', '술집', '회', '치킨', '피자', '버거', '커피', '디저트', '빵', '베이커리', '초밥', '스시', '파스타', '국밥', '막창', '곱창', '해산물', '오마카세']):
        return "음식점"
    # 12. 식품
    if any(k in combined_text for k in ['식품', '간식', '과일', '반찬', '밀키트', '농산물', '수산물', '축산물', '영양제']):
        return "식품"
    # 13. 생활
    if any(k in combined_text for k in ['생활', '가전', '가구', '인테리어', '청소', '세탁', '수리', '보수', '세차', '디테일링']):
        return "생활"
    
    return "기타"
