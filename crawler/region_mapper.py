    @classmethod
    def match_city_abbreviation(cls, city_name: str, text: str) -> bool:
        """
        '포항시' -> '포항'이 텍스트에 포함되어 있는지 검사.
        단, '중구', '서구' 처럼 1글자가 되는 경우는 무분별한 매칭을 막기 위해 
        정확히 '중구'라고 쓰여진 경우만 허용.
        """
        if city_name in text:
            return True
            
        # 마지막 '시','군','구' 제거
        if city_name[-1] in ('시', '군', '구'):
            abbr = city_name[:-1]
            if len(abbr) > 1 and abbr in text:
                return True
                
        return False

    @classmethod
    def normalize(cls, raw_text: str) -> dict:
        """
        raw_text: "[대구 수성] 맛집", "해운대 오마카세", "경북 포항 횟집"
        Returns: { "province": "대구", "city": "수성구", "normalized": "대구 수성구" }
        """
        cleaned_text = re.sub(r'[\[\]\(\)\.,/]', ' ', raw_text).strip()
        
        # 광역지역명 앨리어스 처리 ("전라" -> "전북/전남" 등 모호한 경우 처리를 위함)
        # 1. 명확한 도/시 먼저 탐색
        province = "기타"
        city = "기타"

        # 1. 키워드 매핑 테이블 우선 검사 (가장 빠르고 확실함)
        for key, (prov, ct) in cls.KEYWORD_MAP.items():
            if key in cleaned_text:
                return {"province": prov, "city": ct, "normalized": f"{prov} {ct}"}

        # 2. 명확한 시/도 (Province) 탐색
        found_provinces = []
        for prov in cls.REGIONS.keys():
            if prov in cleaned_text:
                found_provinces.append(prov)
                
        if found_provinces:
            # 먼저 매칭된 것을 우선. (예: "서울 경기" 라면 서울)
            province = found_provinces[0]
            
        # 3. 만약 명확한 시/도가 없고 "전라", "충청", "경상" 같은 묶음말이 있을 때 처리
        # 강남맛집 등에서는 [광주/전라] 묶음 채널이 있음.
        fuzzy_province_map = {
            "전라": ["광주", "전북", "전남"],
            "충청": ["대전", "세종", "충북", "충남"],
            "경상": ["부산", "대구", "울산", "경북", "경남"]
        }
        
        fuzzy_scope = []
        if province == "기타":
            for fuzzy_key, targets in fuzzy_province_map.items():
                if fuzzy_key in cleaned_text:
                    fuzzy_scope = targets
                    break

        # 4. 하위 시/군/구 (City) 탐색
        selected_prov = province
        selected_city = "기타"

        # 4-1. Province가 특정된 경우, 해당 Province 안에서만 탐색
        if province != "기타":
            for ct in cls.REGIONS[province]:
                if cls.match_city_abbreviation(ct, cleaned_text):
                    selected_city = ct
                    break
        
        # 4-2. Province가 불명확하고 전라/충청/경상 등 바운더리만 잡힌 경우
        elif fuzzy_scope:
            for p_candidate in fuzzy_scope:
                for ct in cls.REGIONS[p_candidate]:
                    if cls.match_city_abbreviation(ct, cleaned_text):
                        selected_prov = p_candidate
                        selected_city = ct
                        break
                if selected_city != "기타":
                    break
            
            if selected_prov == "기타":
                # 하위 도시를 못 찾았으면 대표 이름을 부여
                selected_prov = fuzzy_scope[0] # 예: 전라 -> 광주(가장 큰곳 임의 배정) 또는 '기타'로 둘 수 있으나 일단 기타로 처리.
                
        # 4-3. Province에 대한 힌트가 아예 없는 경우 (전체 역탐색)
        else:
            best_prov = "기타"
            best_city = "기타"
            
            for p_candidate, cities in cls.REGIONS.items():
                for ct in cities:
                    if cls.match_city_abbreviation(ct, cleaned_text):
                        if ct in cls.AMBIGUOUS_CITIES:
                            # 모호한 지역명은 도/시가 무조건 명시되거나 다른 명확한 지역 단위와 함께 쓰여야 함.
                            # 단독 추론 시 부산 북구 등을 오탐하므로 무시함.
                            continue
                            
                        best_prov = p_candidate
                        best_city = ct
                        break
                if best_city != "기타":
                    break
                    
            selected_prov = best_prov
            selected_city = best_city

        # 결과를 찾은 경우와 못 찾은 경우 반환
        if selected_prov != "기타" and selected_city != "기타":
            return {"province": selected_prov, "city": selected_city, "normalized": f"{selected_prov} {selected_city}"}
        elif selected_prov != "기타":
            return {"province": selected_prov, "city": "기타", "normalized": selected_prov}
        else:
            return {"province": "기타", "city": "기타", "normalized": raw_text[:10]} # 못찾은 경우 원본의 앞부분만 잘라서 저장
